import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parent.parent))

import arabic_reshaper
from bidi.algorithm import get_display


def shape_arabic(text: str) -> str:
    reshaped = arabic_reshaper.reshape(text)
    return get_display(reshaped)


def build_pdf(filename: str, title: str, sections: list[tuple[str, str]]):
    from fpdf import FPDF

    pdf_path = Path("data/raw") / filename
    font_dir = Path("fonts")
    font_path = font_dir / "NotoSansArabic-Regular.ttf"

    pdf = FPDF(orientation="P", unit="mm", format="A4")
    pdf.set_auto_page_break(auto=True, margin=20)
    pdf.add_page()

    if font_path.exists():
        pdf.add_font("NotoArabic", "", str(font_path))
        font_name = "NotoArabic"
    else:
        font_name = "Helvetica"

    def add_section(title_text: str, body_text: str):
        pdf.set_font(font_name, size=16)
        t = shape_arabic(title_text) if font_name == "NotoArabic" else title_text
        pdf.cell(0, 12, t, new_x="LMARGIN", new_y="NEXT")
        pdf.set_font(font_name, size=12)
        b = shape_arabic(body_text) if font_name == "NotoArabic" else body_text
        pdf.multi_cell(0, 8, b, new_x="LMARGIN", new_y="NEXT")
        pdf.ln(4)

    # Title
    pdf.set_font(font_name, size=22)
    t = shape_arabic(title) if font_name == "NotoArabic" else title
    pdf.cell(0, 20, t, new_x="LMARGIN", new_y="NEXT", align="C")
    pdf.ln(8)

    for sec_title, sec_body in sections:
        add_section(sec_title, sec_body)

    pdf.output(str(pdf_path))
    print(f"Created: {pdf_path} ({pdf_path.stat().st_size / 1024:.1f} KB, {pdf.pages_count} pages)")
    return pdf_path


